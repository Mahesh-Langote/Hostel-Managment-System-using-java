    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String status = (String) jComboBox1.getSelectedItem();
        try {
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement();
             
            if (status.equals("Living")) {
                st.executeUpdate("update room set roomStatus='Booked' where roomNumber='" + jTextField9 + "'");
            } else {
                String statusm = (String) jComboBox1.getSelectedItem();
                st.executeUpdate("update room set roomStatus='Not Booked' where roomNumber='" + jTextField9 + "'");

                PreparedStatement ps = con.prepareStatement("update studentDetails set name=?, fName=?, mName=?, email=?, address=?, branch=?, uid=?, roomNumber=?, status=? where mobailNumber=?");
                ps.setString(1, jTextField2.getText());
                ps.setString(2, jTextField3.getText());
                ps.setString(3, jTextField4.getText());
                ps.setString(4, jTextField5.getText());
                ps.setString(5, jTextField6.getText());
                ps.setString(6, jTextField7.getText());
                ps.setString(7, jTextField8.getText());
                ps.setString(8, jTextField9.getText());
                ps.setString(10, jTextField1.getText());
                ps.setString(9, statusm);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Succesufully Update");
                clear();

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

