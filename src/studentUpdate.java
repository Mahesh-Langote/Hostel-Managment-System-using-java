    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String mobileNumber = jTextField1.getText();
        try {
            Connection con = connectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("select*from studentDetails where mobailNumber='" + mobileNumber + "'");
            if (rs.first()) {
                jTextField1.setVisible(false);
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jTextField6.setText(rs.getString(6));
                jTextField7.setText(rs.getString(7));
                jTextField8.setText(rs.getString(8));
                jTextField9.setText(rs.getString(9));
                jTextField9.setEditable(false);
                if (rs.getString(10).equals("living")) {
                    jComboBox1.addItem("living");
                    jComboBox1.addItem("Leaved");
                } else {
                    jComboBox1.addItem("Leaved");
                    jComboBox1.addItem("living");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Student Not available");
                clear();
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

